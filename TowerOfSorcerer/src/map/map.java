package map;

public class map {
	private int floor;
	private int[][] removable;
	private int[][] unremovable;
	
	public int getFloor() {
		return floor;
	}
	public void setFloor(int floor) {
		this.floor = floor;
	}
	public int[][] getRemovable() {
		return removable;
	}
	public void setRemovable(int[][] removable) {
		this.removable = removable;
	}
	public int[][] getUnremovable() {
		return unremovable;
	}
	public void setUnremovable(int[][] unremovable) {
		this.unremovable = unremovable;
	}


}
